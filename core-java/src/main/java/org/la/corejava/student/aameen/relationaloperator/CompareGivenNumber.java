public class CompareGivenNumber {

	public static void main(String[] args) {
		// wap to compare two numbers and print the result  >=,  <=, !=

		int num1 = 23, num2=34;
	 System.out.println("num1 >= num2 : "+ (num1>=num2));
	 System.out.println("num1 <= num2 : "+ (num1<=num2));
	 System.out.println("num1 != num2 : "+ (num1!=num2));
	}

}
