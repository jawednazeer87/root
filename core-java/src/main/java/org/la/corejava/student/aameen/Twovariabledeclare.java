package org.la.corejava.student.aameen;

public class Twovariabledeclare {

	public static void main(String[] args) {
		// declaring  type of two variable and printing its value 
		
		int a = 12, b = 13;
		System.out.println("the value of declared int variable is  a : " +a + " ; b :" +b);
		System.out.println("   " );
		
		char c = 'A', d = 'S';
		System.out.println("the value of declared char variable is  c : " +c + " ; d : " +d);
		System.out.println("   " );

		boolean f = false , h = true ;
		System.out.println("the value of declared boolean variable is  f : " +f + " ; h : " +h);
		System.out.println("   " );
		
		float v = 12, g = 13;
		System.out.println("the value of declared flaot variable is  v : " +v + " ; g : " +g);
		System.out.println("   " );
		
		byte s = 14, k = 2;
		System.out.println("the value of declared byte variable is  s : " +s + " ; k : " +k);
		System.out.println("   " );
		
		long l = 13L, x = 1L;
		System.out.println("the value of declared Long variable is  l : " +l + " ; x :" +x);
		System.out.println("   " );
		
		double j = 14, p = 20;
		System.out.println("the value of declared double variable is  j : " +j + " ; p :" +p);
		System.out.println("   " );
		
	}

}
