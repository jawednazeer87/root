package org.la.corejava.student.aameen.forloop;

public class Program33 {

	public static void main(String[] args) {
		// wap to print all upper case vowels.
		for(char a ='A';a<='Z';a++)
		{
			if((a == 'A')||(a=='E')||(a=='I')||(a=='O')||(a=='U'))
			{
				System.out.println(a);
			}
				
		}

	}

}
