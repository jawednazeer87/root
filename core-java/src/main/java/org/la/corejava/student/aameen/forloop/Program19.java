package org.la.corejava.student.aameen.forloop;

public class Program19 {

	public static void main(String[] args) {
		// wap to print all numbers between 30 to 80 those are divisible by 6 and 5.
		for(int i =30;i<=80;i++)
		{
			if(i%6==0)
				System.out.println(i);
			else if(i%5==0)
				System.out.println(i);
		}
	}

}
