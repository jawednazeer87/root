package org.la.corejava.student.aameen;

public class Declarevariable {

	public static void main(String[] args) {
		// Declaring types of variable and printing its value
		
		long a = 99L;
		System.out.println("value of long variable  a : " +a);
		System.out.println("  ");
		
		float b = 8f;
		System.out.println("value of float variable b : " +b);
		System.out.println(" ");
		
		double num = 9;
		System.out.println("value of double variable  num : " +num);
		System.out.println(" ");
		
		byte d = 12;
		System.out.println("value of byte variable  d : " +d);
		System.out.println(" ");

		boolean f = false ;
		System.out.println("value of boolean variable  f : " +f);
		System.out.println(" ");
		
		char c = 'A';
		System.out.println("value of char variable  c : " +c);
		System.out.println(" ");



	}

}
