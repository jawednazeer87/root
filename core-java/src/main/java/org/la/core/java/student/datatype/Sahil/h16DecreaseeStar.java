public class h16DecreaseeStar {

	void DecreaseStar() {
		 int i, j, k = 4;
	     for ( i = k; i >= 1; i--) {
	         for ( j = i; j >= 1; j--) {
	             System.out.print("*");
	         }
	         System.out.println();

	     }
		
	}
	
	
	
	
}
