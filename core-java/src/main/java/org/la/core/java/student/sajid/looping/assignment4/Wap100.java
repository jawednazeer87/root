package org.la.core.java.student.sajid.looping.assignment4;

public class Wap100 {

	public static void main(String[] args) {
		int a = 34;
        double d = 20.5;
        d+=a;
        a+=d;
        d-=a;
        a-=d;
        System.out.println(" d = " + ++d );
        System.out.println(" a = " + a-- );

	}

}
