/* 
 * 
 *      1
		1 2
		1 2 3
*/
public class EightyThreePro {
	public static void main(String[] args) {
		int n=3;
		for(int i=1;i<=n;i++)  {
			
		
		
			for(int k=1;k<=i;k++)  {
				
			System.out.print(k+" ");
				
			}
				System.out.println();
						
		}
				
		
	}

		
	
		
}
