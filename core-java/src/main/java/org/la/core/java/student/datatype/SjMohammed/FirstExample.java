package org.la.core.java.student.datatype.SjMohammed;

public class FirstExample {

	public static void main(String[] args) {
		int a= 2;
		long b= 34;
		float c= 3.5f;
		double d= 44.5;
		byte e=1;
		boolean f=true;
		char g='Z';
		System.out.println("The int value: "+a);
		System.out.println("The Long value: "+b);
		System.out.println("The Float value: "+c);
		System.out.println("The Double value: "+d);
		System.out.println("The Byte value: "+e);
		System.out.println("The Boolean value: "+f);
		System.out.println("The Char value: "+g);

	}

}
