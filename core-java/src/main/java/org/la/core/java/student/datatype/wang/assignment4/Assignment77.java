public class Assignment77 {

    /*
   77.
		****
		***
		**
		*
     */
    public static void main(String[] args) {
        int i, j, row = 4;
        for ( i = row; i >= 1; i--) {
            for ( j = i; j >= 1; j--) {
                System.out.print("*");
            }
            System.out.println("");
        }
    }
}