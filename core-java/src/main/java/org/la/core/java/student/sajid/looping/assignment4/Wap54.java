public class Wap54 {

	public static void main(String[] args) {
		int givenNum = 9;
		int limit = 10;
		
		System.out.println("Multiplication table for "+givenNum+" is:");
		for(int i=1;i<=limit;i++) {
			
			int res = givenNum * i;
			
			System.out.println(givenNum+" * "+i+" = "+res);
			
			
		}

	}

}
