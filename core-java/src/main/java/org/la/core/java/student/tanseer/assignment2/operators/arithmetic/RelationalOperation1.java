package org.la.core.java.student.tanseer.assignment2.operators.arithmetic;

public class RelationalOperation1 
{
	public static void main(String[] args) 
	{
		int a = 10 ;
		int b = 20 ;
		
		boolean c;
		c = a > b;
		System.out.println(c); 
		c = a >= b;
		System.out.println(c); 
		c = a < b;
		System.out.println(c); 
		c = a <= b;
		System.out.println(c); 
		c = a == b;
		System.out.println(c); 
		c = a != b;
		System.out.println(c); 
		
		
	}
}
