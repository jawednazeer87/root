public class Wap51 {

	public static void main(String[] args) {
		int n=5;
		int sum=0;
		
		System.out.print("Sum of natual numbers from 1 to "+n+" is : ");
		
		for(int i=1;i<=n;i++) {
			
			sum=sum+i;

		}
		System.out.print(sum);

	}

}
