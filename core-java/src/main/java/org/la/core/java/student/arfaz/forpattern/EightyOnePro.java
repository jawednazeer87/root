public class EightyOnePro {
	public static void main(String[] args) {
		int n=5;
		for(int i=1;i<=n;i++)  {
			for(int spc=n;spc>=i;spc--)  {
			
				System.out.print(" ");
			
			}
		
		
			for(int k=1;k<=i;k++)  {
				
			System.out.print("* ");
				
			}
				System.out.println();
						
		}
				
		
	}

		
	
		
}
