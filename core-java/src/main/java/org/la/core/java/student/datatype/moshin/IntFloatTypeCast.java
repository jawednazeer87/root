public class IntFloatTypeCast {

	public static void main(String[] args) {
		
		int a = 1;
		double f = 9.7F;
		double pro = a * f;
		int mul = (int)(a * f);
		System.out.println(" product : " +pro);
		System.out.println(" product : " +mul);
		
		
	}

}
