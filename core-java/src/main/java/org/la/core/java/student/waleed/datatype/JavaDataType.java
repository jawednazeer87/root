package org.la.core.java.student.waleed.datatype;

public class JavaDataType {

	public static void main(String[] args) {
		byte myNum1 = 107;                       // size 1 byte  Integer types stores whole numbers, positive or negative 
		short myNum2 = 7000;                    // size 2 byte Integer types stores whole numbers, positive or negative
		int myNum3 = 700000;                    //  size 4 bytes Integer types stores whole numbers, positive or negative
		long myNum4 = 700000000;               // Integer 8 bytes  Integer types stores whole numbers, positive or negative
	    float myFloatNum = 9.99f;               // size 4 bytes Stores fractional numbers 
		double myNumd = 15.77;               // size 8 bytes Stores fractional numbers  
		char myLetter = 'W';                    // size 2 byte Stores a single character/letter or ASCII values
		boolean myBool = true;                 //  size 2 bit  store true or false values 
		
		System.out.println("myNum1 "+ myNum1);
		System.out.println("myNum2 "+ myNum2);
		System.out.println("myNum3 "+ myNum3);
		System.out.println("myNum4 "+ myNum4);
		System.out.println("myFloatNum  "+ myFloatNum);
		System.out.println("myNumd  "+myNumd);
		System.out.println("myLetter  "+ myLetter);
		System.out.println("myBool   "+ myBool);
		
	}

}
