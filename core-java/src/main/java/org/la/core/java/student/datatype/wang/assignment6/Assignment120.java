public class Assignment120 {
    /*
   120
	double percent(int, int, int, int, int)
		suppose 5 arguments are marks in subjects, each subject is of 100 marks,
		find percentage and return result
     */
    public double average(int num1, int num2, int num3, int num4, int num5) {
        double result = (num1 + num2 + num3 + num4 + num5) / 500;
        return result;
    }
}
