import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/*
 * 
double div(int, int) - divide first argument with second argument and return the result
*/

public class DivDouble2VarsReturn {
	static Logger log = LoggerFactory.getLogger(DivDouble2VarsReturn.class);

	public static void main(String[] args) {
		double a=12,b=3;
		double c = remainder2Nums(a,b);
		log.info("div 2 nums = "+c);
	}
	static  double remainder2Nums(double a,double n) {
		
			
		return n/a;  	
		}
	
}
