public class Program7 {

	public static void main(String[] args) {
		// 7.	wap to find to find greatest among three numbers
		int i = 10, j = 20, k = 30;
		if ((i > j) && (i > k)) {			
			System.out.println("i is greatest: "+i);
		}
		else if ((j > i) && (j > k)) { 
			System.out.println("j is greatest: "+j);		
		}
		else if ((k > i) && (k > j)) {			
			System.out.println("k is greatest: "+k);		
		}		
	}

}
