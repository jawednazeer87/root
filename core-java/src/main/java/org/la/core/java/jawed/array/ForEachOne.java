package org.la.core.java.jawed.array;

public class ForEachOne {

	public static void main(String[] args) {
		
		int[] a = {1, 2, 3, 4,-1, -4};
		for(int b : a) {
			System.out.println(b);
		}
		
		double[] array = {1.4, 44.2, 2.3, 8.4,-1.7, 0.4};
		for(double b : array) {
			System.out.println(b);
		}
		
	}
}
