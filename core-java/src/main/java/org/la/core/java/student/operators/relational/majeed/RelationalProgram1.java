package org.la.core.java.student.operators.relational.majeed;

public class RelationalProgram1 {

	public static void main(String[] args) {
		// wap to compare two numbers and print the result, >, >=, <, <=, ==, !=
		int a = 10, b = 11;
		
		System.out.println("a > b: "+ (a > b));
		System.out.println("a >= b: "+ (a >= b));
		System.out.println("a < b: "+ (a < b));
		System.out.println("a <= b: "+ (a <= b));
		System.out.println("a == b: "+ (a == b));
		System.out.println("a != b: "+ (a != b));

	}

}
