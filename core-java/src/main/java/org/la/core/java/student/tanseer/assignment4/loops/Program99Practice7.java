public class Program99Practice7 
{
	public static void main(String[] args) 
	{
		int a = 30 ;
		int b = a++ ;
		int c = ++a ;
		System.out.println("b : "+b); 
		System.out.println("a : "+a); 
		System.out.println("c : " +c);
		
	}
}

