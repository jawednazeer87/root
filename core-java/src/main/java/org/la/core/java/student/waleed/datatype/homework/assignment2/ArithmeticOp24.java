package org.la.core.java.student.waleed.datatype.homework.assignment2;

public class ArithmeticOp24 {
	public static void main(String[] args) {
		
		int a = -4;
		a=a+1;  // a = -4 +1 = -3       
		a++;  // a -3 +1 = -2      
		a+=1;    // a = -2 +1 = -1      
		++a;  // a = -1 +1 = 0
		System.out.println(" a = " + a );
		
	}

}
