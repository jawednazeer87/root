package org.la.core.java.student.sajid.array.assignment5;

public class Wap55 {

	public static void main(String[] args) {
		// wap to print all ascii value of character array
		//Suppose given array is ['a', '5', '7', 'p'] 
		
		char arr[] = {'a', '5', '7', 'p'};
		
		System.out.println("ASCII value of each element in character array: ");
		
		for(int i=0;i<arr.length;i++) {
			int j = arr[i];
			System.out.print(j+ " ");
		}

	}

}
