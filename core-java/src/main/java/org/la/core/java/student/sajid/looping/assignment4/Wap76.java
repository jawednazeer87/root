package org.la.core.java.student.sajid.looping.assignment4;

public class Wap76 {

	public static void main(String[] args) {
		// print pattern using loop
		
		char c = '*';
		
		int n = 4;
				
		for(int i=1;i<=n;i++) {
			for(int j=1;j<=i;j++) {
			
			System.out.print(c);
			}
			System.out.println("");
			
		}

	}

}
