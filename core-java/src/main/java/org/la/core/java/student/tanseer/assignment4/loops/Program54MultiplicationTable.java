package org.la.core.java.student.tanseer.assignment4.loops;

public class Program54MultiplicationTable {

	public static void main(String[] args) {
		int n = 7;
		System.out.println("Multiplication table :");
		
		for (int i = 1 ; i <= 10 ; i++) {
			System.out.println(n + " * " + i +" = "+n * i);
		}
	}
}