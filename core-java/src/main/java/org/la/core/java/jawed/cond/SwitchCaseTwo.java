package org.la.core.java.jawed.cond;

public class SwitchCaseTwo {

	public static void main(String[] args) {
		
		int a = 7;
		switch(a) {
			case 1: 
				System.out.println("one");
				break;
			case 2: 
				System.out.println("second");
				break;
			case 3: 
				System.out.println("three");
				break;
			case 4: 
				System.out.println("four");
				break;
		}
	}
}
