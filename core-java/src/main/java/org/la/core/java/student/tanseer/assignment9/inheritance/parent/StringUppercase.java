/*
 * 21. create a parent class having protected String uppercase(String) method, 
 * override uppercase method in child class convert each character to uppercase 
 * and will return uppercase string.
 */

public class StringUppercase 
{
	protected String uppercase(String s)
	{
		return s.toUpperCase();
	}
}
