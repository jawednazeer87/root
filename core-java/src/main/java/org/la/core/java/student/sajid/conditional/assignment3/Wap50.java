public class Wap50 {

	public static void main(String[] args) {
		int upprCaseA = 'A';
		int upprCaseZ = 'Z';
		
		int givenChar = 'D';
		char c ='D';
		
		
		System.out.println("Given char is: "+c);
		
		if (givenChar >= upprCaseA && givenChar <= upprCaseZ) {
			System.out.println("up");
		}else {
			System.out.println("Given char is not uppercase");
		}
				

	}

}
