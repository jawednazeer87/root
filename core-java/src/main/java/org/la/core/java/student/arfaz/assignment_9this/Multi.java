public class Multi {
	private int a;
	private int b;
	public void seta(int a) {
		this.a=a;
	}
	public int geta() {
		return a;
	}
	public void setb(int b) {
		this.b=b;
	}
	public int getb() {
		return b;
	}
	
	public int mul() {
		return a*b;
	}
}
