public class Assignment51 {
    //51.	Wap to find sum of all natural numbers between 1 to n.
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            sum += i;
        }
        System.out.println(sum);
    }
}
