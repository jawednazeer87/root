package org.la.core.java.student.operators_assign.arfaz;

public class TwentyTw {

	public static void main(String[] args) {
		int a = 4;
        a++;
		System.out.println(" a = " + a );

        a += a;
		System.out.println(" a = " + a );

        --a;
		System.out.println(" a = " + a );

        a = 7 + a;
		System.out.println(" a = " + a );

        a *= a;
		System.out.println(" a = " + a );

        a -= 3;
		System.out.println(" a = " + a );
        }

}
