/*
 * wap to print 'up' if any given character is upper case
 */

public class Program76 
{

	public static void main(String[] args) 
	{
		char ch = 'r';
		
		if(ch >= 'A' && ch <= 'Z')
		{
			System.out.println("UP");
		}
		else
			System.out.println("Not Upper case");
	}	

}
