public class Assignment34 {
    //34. wap to print zero if number zero, 1 if positive and -1 if number is negative
    public static void main(String[] args) {
        int a = 11;
        if (a > 0) {
            System.out.println("1");
        } else if (a == 0) {
            System.out.println("0");
        } else {
            System.out.println("-1");
        }
    }
}
