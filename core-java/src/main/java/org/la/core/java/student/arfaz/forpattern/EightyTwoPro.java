public class EightyTwoPro {
	public static void main(String[] args) {
		int n=5;
		for(int i=1;i<=n;i++)  {
			for(int spc=1;spc<i;spc++)  {
			
				System.out.print(" ");
			
			}
		
		
			for(int k=n;k>=i;k--)  {
				
			System.out.print("* ");
				
			}
				System.out.println();
						
		}
				
		
	}

		
	
		
}
