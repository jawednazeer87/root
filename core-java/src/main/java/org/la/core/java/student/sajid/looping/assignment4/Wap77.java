package org.la.core.java.student.sajid.looping.assignment4;

public class Wap77 {

	public static void main(String[] args) {
		// print pattern using loop
		
		char c = '*';
		int n = 4;
		
		for(int i=n;i>=1;i--) {
			for(int j=i;j>=1;j--) {
				System.out.print(c);
			}
			System.out.println("");
		}

	}

}
