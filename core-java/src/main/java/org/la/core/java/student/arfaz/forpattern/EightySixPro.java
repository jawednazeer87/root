	package org.la.core.java.student.arfaz.forpattern;

/* 
 * 
 *       		1
	     2 2
		 	3 3 3
*/
public class EightySixPro {
	public static void main(String[] args) {
		int n=3;
		for(int i=1;i<=n;i++)  {
			
		
		
			for(int k=1;k<=i;k++)  {
				if(i==1&&k==1)
				System.out.print("\t");
				if(i==n&&k==1)
					System.out.print("  ");						
			System.out.print(i+" ");
				
			}
				System.out.println();
						
		}
				
		
	}

		
	
		
}
