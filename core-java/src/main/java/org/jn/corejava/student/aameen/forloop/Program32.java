package org.jn.corejava.student.aameen.forloop;

public class Program32 {

	public static void main(String[] args) {
		// wap to print all lower case vowels.
		for(char a ='a';a<='z';a++)
		{
			if((a == 'a')||(a=='e')||(a=='i')||(a=='o')||(a=='u'))
			{
				System.out.println(a);
			}
				
		}
	}

}
