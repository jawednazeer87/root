package org.jn.corejava.student.aameen.array;

public class ForEachLoop {

	public static void main(String[] args) {
		// 
		int a []= {1,2,3,4,5};
		for(int y :a)
		{
			System.out.println(y);
			}
		System.out.println("");
		
		double b[]= {2.3,3.4,4.5};
		for(double s :b)
		{
			System.out.println(s);
		}
	}

}
