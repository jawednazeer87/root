package org.jn.corejava.student.aameen.arithmetic.operation;

public class PreincreementCode {

	public static void main(String[] args) {
		// wap to do pre increment a number and assign its value to other number and print both number
		int beforenum = 32;
		System.out.println("Number before  pre increement : "+beforenum);
		System.out.println(" ");
		int afternum = ++beforenum;
		System.out.println("Number after pre increement  : "+afternum);
		

	}

}
