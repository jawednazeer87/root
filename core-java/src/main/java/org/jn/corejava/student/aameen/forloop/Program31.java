public class Program31 {

	public static void main(String[] args) {
		// wap to print all lower case alphabets except vowels.
		for(char a='a';a<='z';a++)
		{
			if(a!='a')
					{
					if(a!='e') {
						if(a!='i')
						{
							if(a!='o') {
								if(a!='u')
								{
									System.out.println(a);
								}
							}
						}
							}
					}
					
			}

	}

}
