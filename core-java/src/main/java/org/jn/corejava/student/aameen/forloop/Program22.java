package org.jn.corejava.student.aameen.forloop;

public class Program22 {

	public static void main(String[] args) {
		// wap to print all numbers between 10 to 50 those are divisible by 5 and 9.
		for(int i=10;i<=50;i++)
		{
			if(i%5==0)
				System.out.println(i);
			else if(i%9==0)
				System.out.println(i);
		}

	}

}
