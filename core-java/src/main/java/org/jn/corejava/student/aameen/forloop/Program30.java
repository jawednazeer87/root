public class Program30 {

	public static void main(String[] args) {
		// wap to print all upper case alphabets except vowels.
		for(char a='A';a<='Z';a++)
		{
			if(a!='A')
					{
					if(a!='E') {
						if(a!='I')
						{
							if(a!='O') {
								if(a!='U')
								{
									System.out.println(a);
								}
							}
						}
							}
					}
					
			}

	}

}
