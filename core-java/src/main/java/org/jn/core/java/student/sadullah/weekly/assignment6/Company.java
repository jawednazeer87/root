public class Company {
	int companyID;
	String name;
	String description;
	
	public void setCompanyId(int n) {
		companyID = n;
	}
	
	public void setName(String nme) {
		name = nme;
	}
	
	public void setDescription(String dcrptn) {
		description = dcrptn;
	}
	
	public int getCompanyId() {
		return companyID;
	}
	
	public String getName() {
		return name;
	}
	
	public String getDescription() {
		return description;
	}
}
