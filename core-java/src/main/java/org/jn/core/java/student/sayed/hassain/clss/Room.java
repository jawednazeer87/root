package org.jn.core.java.student.sayed.hassain.clss;

public class Room {

	int roomId;
	int floorNum;
	int seatCapacity;
	
	public int getterRoomId(){
		return roomId;
	}
	public int getterFloorNum() {
		return floorNum;
	}
	public int getterSeatCapacity() {
		return seatCapacity;
	}
	public void setterRoomId(int roomId) {
		this.roomId=roomId;
	}
	public void setterFloorNum(int floorNum) {
		this.floorNum=floorNum;
	}
	public void setterSeatCapacity(int seatCapacity) {
		this.seatCapacity=seatCapacity;
	}
}
