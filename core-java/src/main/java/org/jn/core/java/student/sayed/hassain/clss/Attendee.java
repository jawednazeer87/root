package org.jn.core.java.student.sayed.hassain.clss;

public class Attendee {

	int attendeeId;
	String firstName;
	String lastName;
	int phone;
	String email;
	String vip;
	
	public int getterAttendeeId() {
		return attendeeId;
	}
	public String getterFirstName() {
		return firstName;
	}
	public String getterLastName() {
		return lastName;
	}
	public int getterPhone() {
		return phone;
	}
	public String getterEmail() {
		return email;
	}
	public String getterVip() {
		return vip;
	}
	public void setterAttendeeId(int attendeeId){
		this.attendeeId=attendeeId;
	}
	public void setterFirstName(String firstName) {
		this.firstName=firstName;
	}
	public void setterLastName(String lastName) {
		this.lastName=lastName;
	}
	public void setterPhone(int phone) {
		this.phone=phone;
	}
	public void setterEmail(String email) {
		this.email=email;
	}
	public void setterVip(String vip) {
		this.vip=vip;
	}
}
