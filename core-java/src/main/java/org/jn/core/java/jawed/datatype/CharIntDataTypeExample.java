package org.jn.core.java.jawed.datatype;

public class CharIntDataTypeExample {

	public static void main(String[] args) {
		
		char position = 65;
		System.out.println("position: "+position);
		
		position = (char)-1;
		System.out.println("position: "+position);
		
		int value = '?';
		System.out.println("value: "+value);
	}
}