package org.jn.core.java.student.sayed.hassain.clss;

public class OrderItems {
	
	int orderId;
	int productId;
	int quantity;
	
	public int getterOrderId() {
		return orderId;
	}
	public int getterProductId() {
		return productId;
	}
	public int getterQuantity() {
		return quantity;
	}
	public void setterOrderId(int orderId) {
		this.orderId=orderId;
	}
	public void setterProductId(int productId) {
		this.productId=productId;
	}
	public void setterQuantity(int quantity) {
		this.quantity=quantity;
	}

}
