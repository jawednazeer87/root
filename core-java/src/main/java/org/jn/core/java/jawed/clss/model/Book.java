package org.jn.core.java.jawed.clss.model;

public class Book {

	private int price;
	private String name;
	private String subject;
	
	public void setPrice(int pr) {
		price = pr;
	}
	public int getPrice() {
		return price;
	}
	public void setName(String pr) {
		name = pr;
	}
	public String getName() {
		return name;
	}
	public void setSubject(String sbj) {
		subject = sbj;
	}
	public String getSubject() {
		return subject;
	}
}
