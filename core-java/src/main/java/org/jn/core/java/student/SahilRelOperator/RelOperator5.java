package org.jn.core.java.student.SahilRelOperator;

public class RelOperator5 {
public static void main(String[] args) {
	int a = 10;
	int b = 3;
	boolean x = a++==10 && ++b==4; // && means both are important
	boolean y = --a==10 || b--<4;	// || means alternative one are false then go another  
	System.out.println(a);
	System.out.println(b);
	System.out.println(x);
	System.out.println(y);







}
}
