package org.jn.core.java.student.sadullah.datatype;

public class ArithmeticOperations {

	public static void main(String[] args) {
		int a = 50;
		int b = 70;
		int sum = a+b;
		int subtraction = a-b;
		int multiplication = a*b;
		System.out.println("The sum of a & b is "+sum);
		System.out.println("The difference between a & b is "+subtraction);
		System.out.println("The product of a & b is "+multiplication);		
	}

}
