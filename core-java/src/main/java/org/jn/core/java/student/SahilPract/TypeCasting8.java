package org.jn.core.java.student.SahilPract;

public class TypeCasting8 {
public static void main(String[] args) {
	int i = 19/5; 
	System.out.println("int : "+i);
	char a = 65;
	System.out.println("char : " + a);
	int s = 'a'; 		
	System.out.println("int : "+s);
	double q = 32/5; 
	System.out.println("double : "+q);
	float j = 47/5; 
	System.out.println("float : "+j);
	int sum = 'A' + ' ';
	System.out.println("int : "+sum);
	int sub1 = 'a' - ' ';
	System.out.println("int : "+sub1);
	int sub = 'a' - ' ';
	System.out.println("int : "+sub);
	int sum1 = 'A' + ' ';
	System.out.println("int : "+sum1);
}
}
