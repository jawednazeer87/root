package org.jn.core.java.student.sayed.hassain.clss;

public class SalariedEmployee {
	
	int id;
	int annualSalary;
	int employeeId;
	
	public int getterId() {
		return id;
	}
	public int getterAnnualSalary() {
		return annualSalary;
	}
	public int getterEmployeeId() {
		return employeeId;
	}
	public void setterId(int id) {
		this.id=id;
	}
	public void setterAnnualSalary(int annualSalary) {
		this.annualSalary=annualSalary;
	}
	public void setterEmployeeId(int employeeId) {
		this.employeeId=employeeId;
	}

}
